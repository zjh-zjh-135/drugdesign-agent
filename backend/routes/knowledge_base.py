def retrieve_knowledge(query: str, threshold: float = 0.65) -> str | None:
    """
    检索知识库。先精确匹配，再模糊匹配。
    返回匹配的答案，或 None（未匹配）。
    """
    query = query.strip()
    if not query:
        return None

    # 1. 精确匹配
    if query in KNOWLEDGE_BASE:
        return KNOWLEDGE_BASE[query]

    # 2. 模糊匹配（包含子串）
    for q, ans in KNOWLEDGE_BASE.items():
        if query in q or q in query:
            return ans

    # 3. SequenceMatcher 相似度匹配
    best_score = 0.0
    best_answer = None
    for q, ans in KNOWLEDGE_BASE.items():
        score = SequenceMatcher(None, query, q).ratio()
        if score > best_score:
            best_score = score
            best_answer = ans

    if best_score >= threshold:
        return best_answer

    return None
